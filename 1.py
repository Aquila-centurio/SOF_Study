# Программа для вычисления значения по формуле (вариант 11)

import math

print("Введите значения переменных:")
a = float(input("a = "))
b = float(input("b = "))
x = float(input("x = "))

# Проверка 1: x должно быть больше 0 (иначе ln(x) не существует)
if x <= 0:
    print("Ошибка: x должно быть больше 0, потому что логарифм от неположительного числа не определён.")
else:
    # Проверка 2: выражение под корнем должно быть >= 0
    pod_kornem = (a + b) ** 2 - x
    if pod_kornem < 0:
        print("Ошибка: под корнем отрицательное число. Нельзя извлекать квадратный корень из отрицательного числа.")
    else:
        # Проверка 3: знаменатель не должен быть равен нулю
        znamenatel = a**2 + b * x - x**2
        if znamenatel == 0:
            print("Ошибка: знаменатель равен нулю. Делить на ноль нельзя.")
        else:
            # Всё в порядке — считаем результат
            chislitel = math.log(x) - math.sqrt(pod_kornem)
            resultat = chislitel / znamenatel
            print("Результат =", resultat)
